    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            SimpleDateFormat sdf = new SimpleDateFormat("d MMM yyyy");
            Date date = jDateChooser1.getDate();
            Calendar cal = Calendar.getInstance();
            cal.setTime(date);

            String  tgl = sdf.format(cal.getTime());
            String pk = jTextField1.getText();
            String keterangan = jTextField2.getText();
            String biaya = jTextField3.getText();
            
            
            sql =
                    "INSERT INTO kas(tgl,pk,keterangan,biaya) VALUES('"
                    +tgl+"','"
                    +pk+"','"
                    +keterangan+"','"
                    +biaya+"')";
            stat.executeUpdate(sql);
            stat.close();
            JOptionPane.showMessageDialog(null, "Pencatatan Berhasil");
            
            Relaunch();
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(BukuKas.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

