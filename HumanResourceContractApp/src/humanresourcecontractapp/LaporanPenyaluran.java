    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        //TODO get data from database
        sql = "SELECT\n" +
        "contract.sdm_name,\n" +
        "contract.payment_sdm\n" +
        "FROM\n" +
        "contract ,\n" +
        "constat\n" +
        "WHERE contract.id_contract = '"+jTextField2.getText()+"'";
        try{
            stat = (Statement) connection.prepareStatement(sql);
            rs = stat.executeQuery(sql);

            if(rs.next()){
                String namaSdm = rs.getString("contract.sdm_name");
                String payment = rs.getString("contract.payment_sdm");

                jTextField3.setText(namaSdm);
                jTextField9.setText(payment);

                rs.close();
                stat.close();
            }else{
                JOptionPane.showMessageDialog(null, "Mohon Masukkan nomor Surat Jalan yang sudah Terdaftar!");
            }
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

