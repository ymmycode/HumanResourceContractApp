    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        int response = JOptionPane.showConfirmDialog(this, "Apakah Data Sudah Benar?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        switch (response) {
            case JOptionPane.YES_OPTION:
                try {
                        InputDatabase();
                        JOptionPane.showMessageDialog(null, "Data Berhasil Tersimpan!");
                        Relaunch();
                        this.dispose();
                    } catch (SQLException ex) {
                        Logger.getLogger(LaporanPenyaluran.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(null, "Terdapat Kegagalan!");
                    }
                Relaunch();
                this.dispose();
            break;
            case JOptionPane.NO_OPTION:
            return;
            case JOptionPane.CLOSED_OPTION:
            break;
            default:
            break;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

        private void InputDatabase() throws SQLException {
        // TODO add your handling code here:
        //getvalue then submit
        
        SimpleDateFormat sdf = new SimpleDateFormat("d MMM yyyy");
        Date date = jDateChooser1.getDate();
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        
        String  tgl = sdf.format(cal.getTime());
        String  nosj = jTextField2.getText();
        String  namaSdm = jTextField3.getText();
        String  gaji = jTextField9.getText();
        String  status = buttonGroup1.getSelection().getActionCommand();
        String  jumlah = jTextField5.getText();
        String  setoran = jTextField6.getText();
        String  sertifikat = jTextField7.getText();
        String  seragam = jTextField8.getText();
        String  keterangan = jTextArea1.getText();
        
        sql = "INSERT INTO penyaluran(tanggal, id_contract, sdmName, gaji, status, jumlah, setoran, sertifikat, seragam, keterangan) VALUES ('"
                +tgl+"','"
                +nosj+"','"
                +namaSdm+"','"
                +gaji+"','"
                +status+"','"
                +jumlah+"','"
                +setoran+"','"
                +sertifikat+"','"
                +seragam+"','"
                +keterangan+"')";
        
        stat = (Statement) connection.prepareStatement(sql);
        stat.execute(sql);
        stat.close();
    }
    
